package com.sylvie;

public class UsingSwitch {
    public static void main(String[] args) {
        int n = 6;
        switch (n){
            case 1:
                System.out.println("1");

            case 0:
                System.out.println("0");
                break;
            default:
                System.out.println("other");
                
        }
        System.out.println("end");
    }
}
